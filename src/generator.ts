import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { renderDesign } from "./designer.js";
import type { WebSpec } from "./types.js";

export interface ScaffoldOptions {
  packageName?: string;
  force?: boolean;
}

export interface ScaffoldResult {
  outputDir: string;
  packageName: string;
  moduleName: string;
  command: string;
}

export async function scaffoldCli(spec: WebSpec, outputDir: string, options: ScaffoldOptions = {}): Promise<ScaffoldResult> {
  const root = resolve(outputDir);
  const slug = slugify(options.packageName ?? spec.name);
  const moduleName = slug.replaceAll("-", "_");
  await mkdir(`${root}/src`, { recursive: true });
  await mkdir(`${root}/skills/cli-anything-${slug}`, { recursive: true });
  await write(`${root}/package.json`, generatedPackageJson(slug));
  await write(`${root}/tsconfig.json`, generatedTsconfig());
  await write(`${root}/README.md`, generatedReadme(spec, slug));
  await write(`${root}/WEB2CLI.md`, renderDesign(spec));
  await write(`${root}/src/cli.ts`, generatedCli(spec, slug));
  await write(`${root}/src/spec.ts`, `import type { WebSpec } from "./types.js";\n\nexport const spec: WebSpec = ${JSON.stringify(spec, null, 2)};\n`);
  await write(`${root}/src/types.ts`, generatedTypes());
  await write(`${root}/skills/cli-anything-${slug}/SKILL.md`, generatedSkill(spec, slug));
  return {
    outputDir: root,
    packageName: `cli-anything-${slug}`,
    moduleName,
    command: `cli-anything-${slug}`
  };
}

function slugify(value: string): string {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "") || "web-app";
}

async function write(path: string, content: string): Promise<void> {
  await mkdir(dirname(path), { recursive: true });
  await writeFile(path, content, "utf8");
}

function generatedPackageJson(slug: string): string {
  return `${JSON.stringify({
    name: `cli-anything-${slug}`,
    version: "0.1.0",
    type: "module",
    bin: { [`cli-anything-${slug}`]: "./dist/cli.js" },
    scripts: { build: "tsc -p tsconfig.json" },
    engines: { node: ">=20" },
    devDependencies: { "@types/node": "^24.0.0", "typescript": "^5.9.3" }
  }, null, 2)}\n`;
}

function generatedTsconfig(): string {
  return `${JSON.stringify({
    compilerOptions: {
      target: "ES2022",
      module: "NodeNext",
      moduleResolution: "NodeNext",
      strict: true,
      skipLibCheck: true,
      outDir: "dist",
      rootDir: "src"
    },
    include: ["src/**/*.ts"]
  }, null, 2)}\n`;
}

